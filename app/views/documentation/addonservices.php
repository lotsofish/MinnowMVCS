<em>Release a Service</em>
<p>
	The DB service is provided with the framework. Developers are welcome and encouraged to write services for general release.
</p>

<p>General suggested file structure for service file archive to allow direct extracting to your app folder.</p>
<ul>
	<li>
		system
		<ul>
			<li>
				models
				<ul>
					<li>&lt;any model files for your service&gt;</li>
				</ul>
			</li>
			<li>
				services
				<ul>
					<li>&lt;any service files&gt;</li>
				</ul>
			</li>
		</ul>
	</li>

</ul>

<p>
	Some ideas for services:
</p>
<ul>
<li>User authentication</li>
<li>Language/Internationalization</li>
<li>File upload</li>
<li>Form validation</li>
<li>Image manipulation</li>
<li>Write your own service and release it to the world!</li>
</ul>

